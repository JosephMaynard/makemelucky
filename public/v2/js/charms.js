function awardLuckyCharm(charm){
	var date = new Date();
	luckyVariables.luckStore.charms.push({
		title: charm.title,
		description: charm.description,
		x: charm.x,
		y: charm.y,
		date: date.toISOString()
	});
	appendCharm(luckyVariables.luckStore.charms[luckyVariables.luckStore.charms.length - 1]);
	luckyVariables.charmAwarded = true;
	//console.log(charm);
	fireEvent('Charm Awarded', charm.title);
	console.log('You earned a Luck Charm: '+ charm.title);
}

function appendCharm(charm){
	var date = new Date(Date.parse(charm.date)),
	minutes = 'undefined',
	monthNames = ['January', 'February', 'March', 'April', 'May', 'June', 'July', 'August', 'September', 'October', 'November', 'December'],
	suffix = 'th';
	if(date.getMinutes() < 10){
		minutes = '0' + date.getMinutes();
	} else {
		minutes = date.getMinutes();
	}
	if(date.getDate() == 1 || date.getDate() == 21 || date.getDate() == 31){
		suffix = 'st';
	}
	if(date.getDate() == 2 || date.getDate() == 22){
		suffix = 'nd';
	}
	if(date.getDate() == 3 || date.getDate() == 23){
		suffix = 'rd';
	}
	$('#luckyCharmsContainer').append('<div class="charm"><div class="charmIcon" style="background-position:' + charm.x + '% ' + charm.y + '%"></div><p><span>' +
	charm.title + '</span><br/>' + 
	charm.description + '<br/>' +
	'<b>Awarded:</b> ' + date.getDate() + '<sup>' + suffix + '</sup> ' + monthNames[date.getMonth()] + ' ' + date.getFullYear() + ' - ' + date.getHours() + ':' + minutes +  '</p></div>');
}

